class Attention_block(nn.Module):
    def __init__(self,F_g,F_l,F_int):
        super(Attention_block,self).__init__()
        self.W_g = nn.Sequential(
            nn.Conv2d(F_g, F_int, kernel_size=1,stride=1,padding=0,bias=True),
            nn.BatchNorm2d(F_int)
            )
        
        self.W_x = nn.Sequential(
            nn.Conv2d(F_l, F_int, kernel_size=1,stride=1,padding=0,bias=True),
            nn.BatchNorm2d(F_int)
        )

        self.psi = nn.Sequential(
            nn.Conv2d(F_int, 1, kernel_size=1,stride=1,padding=0,bias=True),
            nn.BatchNorm2d(1),
            nn.Sigmoid()
        )
        
        self.relu = nn.ReLU(inplace=True)
        
    def forward(self,g,x_skip):
        g1 = self.W_g(g)
        x1 = self.W_x(x_skip)
        psi = self.relu(g1+x1)
        psi = self.psi(psi)

        return x_skip*psi
    
    
class UNetUp_attn(nn.Module):
    def __init__(self, in_size, out_size, F_g,F_l,F_int,dropout=0.0):
        
        super(UNetUp_attn, self).__init__()
        layers = [
            nn.ConvTranspose2d(in_size, out_size, 4, 2, 1, bias=False),
            nn.InstanceNorm2d(out_size),
            nn.ReLU(inplace=True),
        ]
        if dropout:
            layers.append(nn.Dropout(dropout))

        self.model = nn.Sequential(*layers)
        
        self.attn=Attention_block(F_g=F_g,F_l=F_l,F_int=F_int)

    def forward(self, x, skip_input):
        x = self.model(x)
        x_attn=self.attn(x,skip_input) #
        x = torch.cat((x_attn, x), 1)

        return x

    
